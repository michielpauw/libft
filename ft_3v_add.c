#include "libft.h"

t_3v	*ft_3v_add(t_3v *v1, t_3v *v2)
{
	t_3v	*v_add;
	int		i;

	if (!v1 || !v2)
		return (NULL);
	if (!(v_add = (t_3v *)malloc(sizeof(t_3v))))
		return (NULL);
	i = 0;
	while (i < 3)
	{
		(v_add->v)[i] = (v1->v)[i] + (v2->v)[i];
		i++;
	}
	return (v_add);
}
