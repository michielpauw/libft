#include "libft.h"

t_3v	*ft_zero_3v_p(void)
{
	t_3v	*v;

	if (!(v = (t_3v *)malloc(sizeof(t_3v))))
		return (NULL);
	(v->v)[0] = 0;
	(v->v)[1] = 0;
	(v->v)[2] = 0;
	return (v);
}
