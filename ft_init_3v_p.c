#include "libft.h"

t_3v	*ft_init_3v_p(double a, double b, double c)
{
	t_3v	*v;

	if (!(v = (t_3v *)malloc(sizeof(t_3v))))
		return (NULL);
	(v->v)[0] = a;
	(v->v)[1] = b;
	(v->v)[2] = c;
	return (v);
}
