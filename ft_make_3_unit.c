#include "libft.h"

void	ft_make_3_unit(t_3v *v)
{
	int		i;
	double	size;

	if (!v)
		return ;
	i = 0;
	size = ft_get_3v_size(*v);
	if (size == 0)
		return ;
	while (i < 3)
	{
		(v->v)[i] /= size;
		i++;
	}
}
